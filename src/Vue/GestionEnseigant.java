    private void bajouterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bajouterActionPerformed
        if (tnom.getText().trim().isEmpty() || tprenom.getText().trim().isEmpty()
                || tage.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this,"champ obligatoire ",
                    "Ereur", JOptionPane.WARNING_MESSAGE);

        } else {
            try {
                s = cn.conectBdd().createStatement();
                s.executeUpdate("insert into enseignant(nom,prenom,age,diplome) values('"
                        + tnom.getText() + "','" + tprenom.getText() 
                        + "','" + tage.getText() + "','"
                        + cbdip.getSelectedItem() + "')");
                JOptionPane.showMessageDialog(this, "Ajout fait avec succes ");
                actualise();
                reset();
            } catch (SQLException ex) {
                Logger.getLogger(GestionEnseigant.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_bajouterActionPerformed

