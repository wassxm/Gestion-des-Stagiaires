    private void bsupprimerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bsupprimerActionPerformed
        if (JOptionPane.showConfirmDialog(this, "Voullez vous vraiment supprimer ?",
                "Confirmation", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
            try {
                s = cn.conectBdd().createStatement();
                s.executeUpdate("delete from enseignant where id='"
                        + tableenseignant.getValueAt(tableenseignant.getSelectedRow(), 0) + "'");
                JOptionPane.showMessageDialog(this, "Suppression avec succes ");
                actualise();
                reset();
            } catch (SQLException ex) {
                Logger.getLogger(GestionEnseigant.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_bsupprimerActionPerformed

