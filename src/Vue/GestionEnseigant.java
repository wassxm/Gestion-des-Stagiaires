    public void select(int ligneslt) {
        tnom.setText(tableenseignant.getValueAt(ligneslt, 1) + "");
        tprenom.setText(tableenseignant.getValueAt(ligneslt, 2).toString());
        tage.setText(tableenseignant.getValueAt(ligneslt, 3).toString());
        cbdip.setSelectedItem(tableenseignant.getValueAt(ligneslt, 4));
    }

    public void reset() {
        tnom.setText(null);
        tprenom.setText(null);
        tage.setText(null);
        cbdip.setSelectedItem("Licence");
    }

