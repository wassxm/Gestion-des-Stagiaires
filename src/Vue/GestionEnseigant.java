    private void brechercherActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_brechercherActionPerformed
        try {
            s = cn.conectBdd().createStatement();
            rs = s.executeQuery("select * from enseignant where nom='"
                    + trecherche.getText() + "'");
            tab.setRowCount(0);
            while (rs.next()) {
                tab.addRow(new Object[]{rs.getObject("id"), rs.getObject("nom"),
                    rs.getObject("prenom"), rs.getObject("age"), rs.getObject("diplome")});
            }
            tableenseignant.setModel(tab);
            if (tab.getRowCount() == 0) {
                JOptionPane.showMessageDialog(this, "l'enseignant n'existe pas ");
                actualise();
            } else {
                select(0);
            }
        } catch (SQLException ex) {
            Logger.getLogger(GestionEnseigant.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_brechercherActionPerformed

